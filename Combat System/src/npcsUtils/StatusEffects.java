package npcsUtils;

public enum StatusEffects {
    FIRE, // reduces  Damage for x Turns
    POISON, // reduces Armor for x Turns
    BLEED, // reduces HP for x Turns
    FROST, // cannot cast Spells for x Turns
    ELECTRIC, // Stun for x Turns
    AIR, // Gives  x% Dodge for x Turns
    QUAKE, // Confuse , increase mana cost of Spells for x Turns
    VOID // reduces Mana Regeneration for x Turns
}
